export default function EcosystemSection() {
  const sectionRef = useRef(null);
  const [isVisible, setIsVisible] = useState(false);

  useEffect(() => {
    const observer = new IntersectionObserver(
      ([entry]) => { if (entry.isIntersecting) setIsVisible(true); },
      { threshold: 0.1 }
    );
    if (sectionRef.current) observer.observe(sectionRef.current);
    return () => observer.disconnect();
  }, []);

  const reveal = (delay = 0) =>
    `landing-reveal${delay ? ` landing-reveal--delay-${delay}` : ''}${isVisible ? ' is-visible' : ''}`;

  return (
    <section className="ecosystem-section" id="ecosystem" ref={sectionRef}>
      <div className={`ecosystem-section__header ${reveal()}`}>
        <p className="section-label">The Ecosystem</p>
        <h2 className="ecosystem-section__title">
          Every technology. Tracked.
        </h2>
        <p className="ecosystem-section__subtitle">
          Ascend tracks market demand for your skills, helping you make smarter learning decisions.
        </p>
      </div>

      {/* Logo grid — two rows */}
      <div className={`ecosystem-logos ${reveal(1)}`}>
        {TECHNOLOGIES.map((tech) => (
          <div
            className="eco-logo"
            key={tech.name}
            title={tech.name}
            style={{ '--brand-color': tech.color }}
          >
            <span className="eco-logo__icon">{tech.svg}</span>
            <span className="eco-logo__name">{tech.name}</span>
          </div>
        ))}
      </div>

      {/* Subtle descriptor line */}
      <p className={`ecosystem-section__note ${reveal(2)}`}>
        Demand scores updated weekly from real job postings and hiring data.
      </p>
    </section>
  );
}
